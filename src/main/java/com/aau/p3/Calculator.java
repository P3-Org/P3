package com.aau.p3;

public class Calculator {

    static int add(int a, int b) {
        return a + b;
    }

    static int multiply(int a, int b) {
        return a * b;
    }

    static int subtract(int a, int b) {
        return a - b;
    }
  
    static int divide(int a, int b) {
        return a / b;
    }

    static int modulo(int a, int b) {
        return a % b;
    }
    // Comment here
}
